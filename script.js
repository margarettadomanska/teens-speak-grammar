let currentTopic = null;
let filteredCards = [];
let currentIndex = 0;
let showingFollowUp = false;
let isAnimating = false;

const topicList = document.getElementById("topic-list");
const cardElement = document.getElementById("card");
const counterElement = document.getElementById("card-counter");

const topics = [...new Set(grammarData.map(item => item.Topic))];

topics.forEach(topic => {
  const btn = document.createElement("button");
  btn.innerText = topic;
  btn.onclick = () => selectTopic(topic, btn);
  topicList.appendChild(btn);
});

function selectTopic(topic, buttonElement) {
  document.querySelectorAll("#topic-list button")
    .forEach(btn => btn.classList.remove("active-topic"));

  buttonElement.classList.add("active-topic");

  currentTopic = topic;
  filteredCards = grammarData.filter(item => item.Topic === topic);
  currentIndex = 0;
  showingFollowUp = false;
  displayCard(true);
}

function displayCard(initial = false) {
  if (!filteredCards.length) return;

  const card = filteredCards[currentIndex];

  cardElement.innerHTML = `
    <h2>${card["Main Question"]}</h2>
    ${showingFollowUp
        ? `<p>${card["Follow-up Question"]}</p>`
        : `<p class="hint">Tap to reveal follow-up</p>`}
  `;

  counterElement.innerText =
    `${currentIndex + 1} / ${filteredCards.length}`;

  if (!initial) {
    cardElement.style.transform = "translateY(0)";
    cardElement.style.opacity = "1";
  }
}

function animate(direction) {
  if (isAnimating) return;
  isAnimating = true;

  const moveOut = direction === "up" ? "-80px" : "80px";

  cardElement.style.transition = "all 0.3s ease";
  cardElement.style.transform = `translateY(${moveOut})`;
  cardElement.style.opacity = "0";

  setTimeout(() => {
    if (direction === "up" && currentIndex < filteredCards.length - 1) {
      currentIndex++;
    }
    if (direction === "down" && currentIndex > 0) {
      currentIndex--;
    }

    showingFollowUp = false;
    displayCard();

    cardElement.style.transition = "none";
    cardElement.style.transform =
      direction === "up" ? "translateY(80px)" : "translateY(-80px)";
    cardElement.style.opacity = "0";

    setTimeout(() => {
      cardElement.style.transition = "all 0.3s ease";
      cardElement.style.transform = "translateY(0)";
      cardElement.style.opacity = "1";
      isAnimating = false;
    }, 20);

  }, 300);
}

function nextCard() {
  animate("up");
}

function prevCard() {
  animate("down");
}

function randomCard() {
  if (!filteredCards.length) return;
  currentIndex = Math.floor(Math.random() * filteredCards.length);
  showingFollowUp = false;
  displayCard();
}

function togglePresentation() {
  document.body.classList.toggle("presentation-mode");
}

cardElement.addEventListener("click", () => {
  showingFollowUp = !showingFollowUp;
  displayCard();
});

const hammer = new Hammer(cardElement);
hammer.get('swipe').set({ direction: Hammer.DIRECTION_VERTICAL });

hammer.on("swipeup", nextCard);
hammer.on("swipedown", prevCard);

document.addEventListener("keydown", function(event) {
  if (event.key === "Escape") {
    document.body.classList.remove("presentation-mode");
  }
});

